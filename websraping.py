def get_resume_links(category_path):
    links = set()
    page_num = 1

    while len(links) < MAX_PER_CATEGORY:
        page_url = f"{BASE_URL}{category_path}/page/{page_num}"
        print(f"Fetching page: {page_url}")

        response = requests.get(page_url, headers=HEADERS)
        soup = BeautifulSoup(response.text, "html.parser")

        table = soup.find("table", class_="table table-border hit-table")
        if not table:
            print("No table on page — stopping.")
            break

        page_links = table.find_all("a", href=True)
        if not page_links:
            print("No links found — stopping.")
            break

        before_count = len(links)
        for link in page_links:
            full_link = urljoin(BASE_URL, link['href'])
            links.add(full_link)

        # if no new links were added, stop pagination
        if len(links) == before_count:
            break

        page_num += 1
        time.sleep(DELAY)

    return list(links)[:MAX_PER_CATEGORY]


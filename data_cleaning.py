    # 3️⃣ Remove emails
    # -----------------------------
    text = re.sub(r'\S+@\S+', '', text)

    # -----------------------------
    # 4️⃣ Remove phone numbers
    # -----------------------------
    text = re.sub(r'\+?\d[\d\-\s]{8,15}', '', text)

    # -----------------------------
    # 5️⃣ Remove extra spaces

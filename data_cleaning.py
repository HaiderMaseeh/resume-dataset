import re

def clean_resume_text(text):

    if pd.isna(text):
        return ""

    # Remove emails
    text = re.sub(r'\S+@\S+', '', text)

    # Remove phone numbers
    text = re.sub(r'\+?\d[\d\-\s]{8,15}', '', text)

    # Remove everything after footer keywords
    footer_keywords = [
        "Hire Now",
        "Report an issue",
        "Privacy Policy",
        "Resume Categories",
        "Contact Schedule",
        "Loading, Please wait"
    ]

    for word in footer_keywords:
        if word in text:
            text = text.split(word)[0]

    # Remove website header noise
    garbage_patterns = [
        "Hire IT People",
        "Employee Forms",
        "Client Services",
        "IT Staff Augmentation",
        "Browse Jobs",
        "Upload Resume",
        "Visa Sponsorship",
        "Follow Us"
    ]

    for pattern in garbage_patterns:
        text = text.replace(pattern, "")

    # Remove weird symbols
    text = re.sub(r'[^a-zA-Z0-9\s.,:/()-]', '', text)

    # Remove extra spaces
    text = re.sub(r'\s+', ' ', text)

    return text.strip()

